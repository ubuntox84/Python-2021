#Hallar el numero de vocales escritas en la siguiente expresion 
cad="hola fiis - unas"
print(type(cad))
lista=list(cad)
print(lista)
print ("Nr de vocales de la letra a es {}".format(lista.count('a')))
print ("Nr de vocales de la letra e es {}".format(lista.count('e')))
print ("Nr de vocales de la letra i es {}".format(lista.count('i')))
print ("Nr de vocales de la letra o es {}".format(lista.count('o')))
print ("Nr de vocales de la letra u es {}".format(lista.count('u')))
